public class LCR189MechanicalAccumulator {

    public int mechanicalAccumulator(int target) {
        return dfs(target);
    }

    private int dfs(int target) {
        boolean flag = target > 0 && (target += dfs(target - 1)) > 0;
        return target;
    }
}

